public class Book {
    private String name;
    private String author;
    private String genre;
    private String isbn;
    private String language;

    public Book(String name, String author, String genre, String isbn, String language) {
        this.name = name;
        this.author = author;
        this.genre = genre;
        this.isbn = isbn;
        this.language = language;
    }

}
